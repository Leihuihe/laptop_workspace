/*
 * 
 *  Ex_2_9.java -> L2-6
 * 
 *  Read int value then write 4 different statements that
 *    each add 1 (increment) i.
 */

public class Ex_2_9
{

	public static void main(String[] args)
	{
		int i = 0;
		StdOut.println(i);
		
		i++;
		StdOut.println(i);
		
		++i;
		StdOut.println(i);
		
		i = i + 1;
		StdOut.println(i);
		
		i += 1;
		StdOut.println(i);
	}

}
